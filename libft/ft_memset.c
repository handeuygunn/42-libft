#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char	*a;
	size_t	i;

	i = 0;
	a = (char *) s;
	while (i < len)
	{
		a [i] = c;
		i++;
	}
	return (s);
}
